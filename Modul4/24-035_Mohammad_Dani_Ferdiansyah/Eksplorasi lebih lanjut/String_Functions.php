<!-- trim()
Menghapus spasi atau karakter yang tidak diinginkan dari awal dan akhir string: -->
<?php
$string = "   Hello World!   ";
echo trim($string);  
echo "<br>";
?>

<!-- strtolower()
Mengubah string menjadi huruf kecil: -->
<?php
$string = "Hello World!";
echo strtolower($string);  
echo "<br>";
?>

<!-- strtoupper()
Mengubah string menjadi huruf besar: -->
<?php
$string = "Hello World!";
echo strtoupper($string);  
?>